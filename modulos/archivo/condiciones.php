diabetesalud.com ofrece a su empresa la posibilidad de realizar campañas de publicidad en nuestro portal web.

diabetesalud.com es un portal en crecimiento desde su fundación con un número creciente de usuarios en general y servicios de profesionales de la medicina.

Estamos especialmente dedicados a los temas relacionados con la salud y la diabetes. Por lo que si su empresa o sus servicios son también relacionados con estos temas, desde Diabetesalud.com le ofrecemos una gran oportunidad para darlos a conocer.

Los anuncios se calculan por número de publicaciones (CPM) por lo que usted solamente paga por cada vez que ven su anuncio en nuestro portal. Además le informaremos del número de visualizaciones y click realizados en su anuncio.

Los precios dependen de la posición del banner (anuncio), tamaño y en que páginas se muestra. Para más información consúltenos sin compromiso a través del formulario de contacto.


Condiciones:

    Formatos. Los banners en formatos gif, gif animado y jpg no deben exceder los 50 Kb, en caso de animaciones flash el máximo permitido es 80Kb.
    Método de pago. Los pagos se reciben desde cualquier país por medio de PayPal y por transferencia bancaria en España.
    Aceptación. Todas las propuestas serán revisadas para su aprobación antes de procesar por completo el correspondiente pago, una vez la campaña ha sido aceptada no habrá devoluciones bajo ningún concepto.

